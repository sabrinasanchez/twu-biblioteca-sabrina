package com.twu.biblioteca;

public class User {
    // Fields
    private String ID;
    private String name;
    private String email;
    private String number;

    // Constructor
    User(String ID, String name, String email, String number){
        this.ID = ID;
        this.name = name;
        this.email = email;
        this.number = number;
    }
    
}
